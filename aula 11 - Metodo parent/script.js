
//Metodo parent
$('p').parent().css({'border':'1px solid #f00'}) //Aplicada uma borda vermelha no elemento PAI do <p>, que é o <li>
$('li').parent().css({'border':'1px solid #00f'})//Aplicada uma borda azul no elemento PAI do <li>, que é o <UL>
$('ul').parent().css({'border':'1px solid #0f0'})//Aplicada uma borda verde no elemento PAI do <ul>, que é o <div>
$('div').parent().css({'border':'1px solid #444'})//Aplicada uma borda azul no elemento PAI do <div>, que é o <body>
$('body').parent().css({'border':'1px solid #f0f'})//Aplicada uma borda azul no elemento PAI do <body>, que é o <html>
